    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nomeUsuario = jTextField1.getText();
        String senha = new String(jPasswordField1.getPassword());

        CadastroService cadastroService = new CadastroService();
        boolean cadastradoComSucesso = cadastroService.cadastrarUsuario(nomeUsuario, senha);

        if (cadastradoComSucesso) {
            JOptionPane.showMessageDialog(this, "Cadastro efetuado com sucesso.");
            Login loginFrame = new Login();
            loginFrame.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Nome de usuário já existe. Escolha outro nome de usuário.");
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

